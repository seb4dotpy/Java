public class DAtaTypes {
    public static void main(String[] args) {

        int integer = 12; //Es limitado a 10 digitos, ocupa 4 bytes de memoria y su rango es de -2147483648 a 2147483647
        byte byteVariable = 34; //Ocupa 1 byte de memoria y su rango es de -128 a 127
        short shortVariable = 32; //Ocupa 2 bytes de memoria y su rango es de -32768 a 32727
        long longVariable = 32; //Ocupa 8 bytes de memoria y su rango es -9223372036854775808 a 9223372036854775807, para diferenciarlo debemos usar una L al final
        //ejemplo
        long longL = 34234278346287346L; //Asi se declara la variable long
        float floatVariableF = 32.5F; //Ocupan 4 bytes pueden ser numeros gigantes
        double doubleVariableD = 33.54;//Ocupa 8 bytes pueden ser numeros gigantes, mucho mas


    }
}
